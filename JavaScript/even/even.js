///To test even odd ///

var num = Number(prompt('Enter Any Number:'))
if (num==0){
  console.log('Entered Number is Zero')
  alert('Entered Number is Zero')

}

else if (num%2==0){
  console.log('Entered Number is Even')
  alert('Entered Number is Even')
}

else{
  console.log('Entered Number is Odd')
  alert('Entered Number is Odd')

}
